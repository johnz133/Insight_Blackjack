
public class Player {
	private String name;
	private int money;
	
	//Set Methods
	public void setName(String name) {
		this.name = name;
	}
	
	public void setMoney(int money) {
		this.money = money;
	}
	
	//Get Methods
	public String getName() {
		return name;
	}
	
	public int getMoney() {
		return money;
	}
	
}
