
public class Player {
	private String name;
	private int money;
	
	//Set Methods
	public void setName(String userName) {
		name = userName;
	}
	
	public void setMoney(int amount) {
		money = amount;
	}
	
	//Get Methods
	public String getName() {
		return name;
	}
	
	public int getMoney() {
		return money;
	}
	
}
